package br.com.rodrigo.integrador.modelo;

public class Aluno {
	
	private int codigoAluno;
	private String nome;
	private String dataNascimento;
	private int codigoSexo;
	private int codigoEstadoCivil;
	private String endereco;
	private String numero;
	private String bairro;
	private int codigoEstado;
	private String celular;
	private String telefone;
	private String email;
	private String cpf;
	private String rg;
	private String senha;
	
	public int getCodigoAluno() {
		return codigoAluno;
	}
	public void setCodigoAluno(int codigoAluno) {
		this.codigoAluno = codigoAluno;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDataNascimento() {
		return dataNascimento;
	}
	public void setDataNascimento(String dataNascimento) {
		this.dataNascimento = dataNascimento;
	}
	public int getCodigoSexo() {
		return codigoSexo;
	}
	public void setCodigoSexo(int codigoSexo) {
		this.codigoSexo = codigoSexo;
	}
	public int getCodigoEstadoCivil() {
		return codigoEstadoCivil;
	}
	public void setCodigoEstadoCivil(int codigoEstadoCivil) {
		this.codigoEstadoCivil = codigoEstadoCivil;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public int getCodigoEstado() {
		return codigoEstado;
	}
	public void setCodigoEstado(int codigoEstado) {
		this.codigoEstado = codigoEstado;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getRg() {
		return rg;
	}
	public void setRg(String rg) {
		this.rg = rg;
	}
	
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}

}
