package br.com.rodrigo.integrador.modelo;

public class Menu {

	int codMenu;
	String nomeMenu;
	String descricaoMenu;
	String tipoMenu;
	String ativoMenu;
	int nivelMenu;
	String urlMenu;
	int ordemMenu;
	int codMenuPai;
	public int getCodMenu() {
		return codMenu;
	}
	public void setCodMenu(int codMenu) {
		this.codMenu = codMenu;
	}
	public String getNomeMenu() {
		return nomeMenu;
	}
	public void setNomeMenu(String nomeMenu) {
		this.nomeMenu = nomeMenu;
	}
	public String getDescricaoMenu() {
		return descricaoMenu;
	}
	public void setDescricaoMenu(String descricaoMenu) {
		this.descricaoMenu = descricaoMenu;
	}
	public String getTipoMenu() {
		return tipoMenu;
	}
	public void setTipoMenu(String tipoMenu) {
		this.tipoMenu = tipoMenu;
	}
	public String getAtivoMenu() {
		return ativoMenu;
	}
	public void setAtivoMenu(String ativoMenu) {
		this.ativoMenu = ativoMenu;
	}
	public int getNivelMenu() {
		return nivelMenu;
	}
	public void setNivelMenu(int nivelMenu) {
		this.nivelMenu = nivelMenu;
	}
	public String getUrlMenu() {
		return urlMenu;
	}
	public void setUrlMenu(String urlMenu) {
		this.urlMenu = urlMenu;
	}
	public int getOrdemMenu() {
		return ordemMenu;
	}
	public void setOrdemMenu(int ordemMenu) {
		this.ordemMenu = ordemMenu;
	}
	public int getCodMenuPai() {
		return codMenuPai;
	}
	public void setCodMenuPai(int codMenuPai) {
		this.codMenuPai = codMenuPai;
	}
}
